package com.org.monitor.model;

public class MomeryModel {

  private String vmFree;
  private String vmUse;
  private String vmTotal;
  private String vmMax;
  private String os;
  private String physicalFree;
  private String physicalTotal;
  private String physicalUse;
  private String totalThread;

  public String getVmFree() {
    return vmFree;
  }

  public void setVmFree(String vmFree) {
    this.vmFree = vmFree;
  }

  public String getVmUse() {
    return vmUse;
  }

  public void setVmUse(String vmUse) {
    this.vmUse = vmUse;
  }

  public String getVmTotal() {
    return vmTotal;
  }

  public void setVmTotal(String vmTotal) {
    this.vmTotal = vmTotal;
  }

  public String getVmMax() {
    return vmMax;
  }

  public void setVmMax(String vmMax) {
    this.vmMax = vmMax;
  }

  public String getOs() {
    return os;
  }

  public void setOs(String os) {
    this.os = os;
  }

  public String getPhysicalFree() {
    return physicalFree;
  }

  public void setPhysicalFree(String physicalFree) {
    this.physicalFree = physicalFree;
  }

  public String getPhysicalTotal() {
    return physicalTotal;
  }

  public void setPhysicalTotal(String physicalTotal) {
    this.physicalTotal = physicalTotal;
  }

  public String getPhysicalUse() {
    return physicalUse;
  }

  public void setPhysicalUse(String physicalUse) {
    this.physicalUse = physicalUse;
  }

  public String getTotalThread() {
    return totalThread;
  }

  public void setTotalThread(String totalThread) {
    this.totalThread = totalThread;
  }
}
